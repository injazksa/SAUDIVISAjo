# ========== تحديد نوع المهنة ==========

def should_exclude(title):
    """استبعاد المهن الحكومية والملكية والمحامين والأطباء والقضاة"""
    exclude_keywords = [
        # أطباء
        'طبيب', 'دكتور', 'طبيبة',
        # محامين وقضاة
        'محامي', 'محاماة', 'قاضي', 'قاض',
        # حكومة
        'وزير', 'حكومة', 'حكومي', 'نائب عام', 'وكيل وزارة',
        # ملكية
        'شيخ', 'أمير', 'ملك', 'أميرة', 'سفير', 'قنصل',
        'المفتي', 'عضو مجلس', 'مندوب دائم',
        # إضافات
        'عضو المجلس الأعلى', 'رئيس قرية'
    ]
    
    title_lower = title.lower()
    for kw in exclude_keywords:
        if kw in title_lower:
            return True
    return False

def is_female_role(title):
    """تحديد المهن النسائية"""
    female_keywords = [
        'منسق زهور', 'منسقة زهور',
        'بائعة', 'كاشيرة',
        'مساعدة إدارية',
        'عاملة منزلية', 'خادمة',
        'نادلة', 'سكرتيرة', 'منسقة'
    ]
    title_lower = title.lower()
    for kw in female_keywords:
        if kw in title_lower:
            return True
    return False

def determine_profession_requirements(title, code):
    """تحديد الأوراق المطلوبة حسب المهنة"""
    title_lower = title.lower()
    
    # 1. رئيس تنفيذي ومدير عام فقط
    if title_lower in ['رئيس تنفيذي', 'مدير عام']:
        return get_requirements_for_ceo()
    
    # 2. سائق خاص
    if 'سائق خاص' in title_lower:
        return get_requirements_for_driver()
    
    # 3. حلاق، طاهي، شيف (مزاولة مهنة)
    if any(word in title_lower for word in ['حلاق', 'طاهي', 'شيف', 'طباخ', 'خباز']):
        return get_requirements_for_barber()
    
    # 4. العمال (كل ما يبدأ بـ "عامل" أو يحتوي على كلمات عمالية)
    worker_keywords = ['عامل', 'عاملة', 'حارس', 'نجار', 'حداد', 'سباك', 'دهان', 'كهربائي', 'نادل']
    if any(title_lower.startswith(kw) or kw in title_lower for kw in worker_keywords):
        # استثناء: عامل منزلي وحارس شخصي لهم معاملة خاصة
        if 'عامل منزلي' in title_lower or 'عاملة منزلية' in title_lower:
            return get_requirements_for_workers()
        if 'حارس شخصي' in title_lower:
            return get_requirements_for_workers()
        return get_requirements_for_workers()
    
    # 5. الإناث
    if is_female_role(title):
        return get_requirements_for_female_workers()
    
    # 6. المهندسين
    if 'مهندس' in title_lower or 'هندسة' in title_lower or 'هندسي' in title_lower:
        return get_requirements_for_engineers()
    
    # 7. Default: مدير تجاري (90% من المهن)
    return get_requirements_for_commercial_manager()

# ========== المعالجة الرئيسية ==========

def process_all_professions():
    """معالجة جميع المهن"""
    # قراءة الملف
    with open('/app/professions_raw.txt', 'r', encoding='utf-8') as f:
        lines = f.read().strip().split('\n')
    
    professions_data = []
    for line in lines:
        if '|' in line:
            parts = line.split('|')
            if len(parts) == 2:
                code, title = parts
                # تجاهل النصوص الإنجليزية
                if any(c.isalpha() and ord(c) < 128 for c in title):
                    continue
                professions_data.append({'code': code.strip(), 'title': title.strip()})
    
    print(f"📂 عدد المهن المستخرجة من الملف: {len(professions_data)}")
    
    # إضافة المهن المفقودة
    missing_professions = [
        {'code': '9991', 'title': 'سائق خاص'},
        {'code': '9992', 'title': 'عامل منزلي'},
        {'code': '9993', 'title': 'حارس شخصي'}
    ]
    
    for mp in missing_professions:
        # تحقق من عدم وجودها
        if not any(p['title'] == mp['title'] for p in professions_data):
            professions_data.append(mp)
            print(f"➕ أضفت: {mp['title']}")
    
    processed = []
    excluded_count = 0
    
    for item in professions_data:
        title = item['title']
        code = item['code']
        
        # استبعاد المهن غير المطلوبة
        if should_exclude(title):
            excluded_count += 1
            print(f"❌ استُبعد: {title}")
            continue
        
        # تحديد الأوراق المطلوبة
        requirements = determine_profession_requirements(title, code)
        
        # إنشاء كائن المهنة
        profession = {
            'code': code,
            'name_ar': title,
            'profession_name_ar': title,
            'profession_code': code,
            'category': 'أخرى',
            'requirements': requirements
        }
        
        processed.append(profession)
    
    print(f"\n📊 الإحصائيات:")
    print(f"   - المهن المستبعدة: {excluded_count}")
    print(f"   - المهن المعالجة: {len(processed)}")
    
    # حفظ النتائج
    with open('/app/frontend/public/professions.json', 'w', encoding='utf-8') as f:
        json.dump(processed, f, ensure_ascii=False, indent=2)
    
    print(f"\n✅ تم الحفظ بنجاح في: /app/frontend/public/professions.json")
    
    # عرض عينات
    print("\n=== 📋 عينات ===")
    samples = [
        next((p for p in processed if 'سائق خاص' in p['name_ar']), None),
        next((p for p in processed if 'رئيس تنفيذي' == p['name_ar']), None),
        next((p for p in processed if 'مهندس' in p['name_ar']), None),
        next((p for p in processed if 'عامل' in p['name_ar']), None),
        next((p for p in processed if 'مدير تجاري' == p['name_ar']), None),
    ]
    
    for sample in samples:
        if sample:
            print(f"\n🔹 {sample['name_ar']} [{sample['code']}]")
            print(f"   عدد الأوراق: {len(sample['requirements'])}")
            print(f"   أول 3 أوراق:")
            for i, req in enumerate(sample['requirements'][:3], 1):
                print(f"      {i}. {req[:60]}...")

if __name__ == '__main__':
    process_all_professions()
