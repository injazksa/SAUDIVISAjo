# ========== تحديد الجنس ==========

def is_female_profession(title):
    """تحديد إذا كانت المهنة نسائية"""
    female_indicators = [
        'ة ', ' ة', 'مساعدة', 'منسقة', 'بائعة', 'كاشيرة', 
        'سكرتيرة', 'عاملة', 'خادمة', 'نادلة', 'موظفة'
    ]
    return any(indicator in title for indicator in female_indicators)

# ========== استبعاد المهن ==========

def should_exclude(title):
    """استبعاد المهن الحكومية والملكية والأطباء والمحامين والقضاة"""
    exclude_keywords = [
        # أطباء
        'طبيب', 'دكتور', 'طبيبة',
        # محامين وقضاة
        'محامي', 'محاماة', 'قاضي', 'قاض', 'محكم', 'كاتب عدل', 'كاتب ضبط',
        # حكومة وملكية
        'وزير', 'نائب وزير', 'وكيل وزارة',  'حكومة', 'حكومي',
        'أمير', 'شيخ', 'ملك', 'أميرة', 'سفير', 'قنصل',
        'محافظ', 'حاكم', 'عمدة',
        # إضافات
        'المفتي', 'النائب العام', 'عضو مجلس', 'عضو المجلس',
        'مندوب دائم', 'رئيس قرية', 'أمين عام', 'أمين مجلس',
        'رئيس بلدية', 'رئيس الأطباء'
    ]
    
    title_lower = title.lower()
    return any(kw in title_lower for kw in exclude_keywords)

# ========== تحديد المتطلبات ==========

def determine_requirements(title, is_female):
    """تحديد الأوراق المطلوبة حسب المهنة والجنس"""
    title_lower = title.lower()
    
    # 1. رئيس تنفيذي ومدير عام فقط
    if title_lower in ['رئيس تنفيذي', 'مدير عام']:
        return get_requirements_for_ceo()
    
    # 2. سائق خاص
    if 'سائق خاص' in title_lower:
        return get_requirements_for_driver_male()
    
    # 3. حلاق، طاهي، شيف (مزاولة مهنة)
    if any(word in title_lower for word in ['حلاق', 'طاهي', 'شيف', 'طباخ', 'خباز']):
        return get_requirements_for_barber()
    
    # 4. العمال
    worker_keywords = ['عامل', 'عاملة', 'حارس', 'نجار', 'حداد', 'سباك', 'دهان', 'كهربائي', 'نادل', 'بناء']
    if any(title_lower.startswith(kw) or kw in title_lower for kw in worker_keywords):
        if is_female:
            return get_requirements_for_female_workers()
        else:
            return get_requirements_for_workers_male()
    
    # 5. المهندسين
    if 'مهندس' in title_lower or 'هندسة' in title_lower or 'هندسي' in title_lower:
        return get_requirements_for_engineers(is_female)
    
    # 6. Default: مدير تجاري
    if is_female:
        return get_requirements_for_commercial_manager_female()
    else:
        return get_requirements_for_commercial_manager_male()

