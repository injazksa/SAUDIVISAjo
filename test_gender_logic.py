def process_all_professions():
    """معالجة جميع المهن"""
    print("⏳ جاري تحميل المهن...")
    
    # هنا سأحمل البيانات من ملف مؤقت
    # في الواقع يجب استخدام البيانات المستخرجة من PDF
    professions_data = load_professions_from_extracted()
    
    print(f"📂 عدد المهن المستخرجة: {len(professions_data)}")
    
    # إضافة المهن المفقودة
    missing_professions = [
        {'code': '999001', 'title': 'سائق خاص'},
        {'code': '999002', 'title': 'عامل منزلي'},
        {'code': '999003', 'title': 'حارس شخصي'},
        {'code': '213791', 'title': 'مندوب مبيعات'}  # المهنة المفقودة
    ]
    
    for mp in missing_professions:
        if not any(p['title'] == mp['title'] for p in professions_data):
            professions_data.append(mp)
            print(f"➕ أضفت: {mp['title']} [{mp['code']}]")
    
    processed = []
    excluded_count = 0
    excluded_list = []
    
    for item in professions_data:
        title = item['title']
        code = item['code']
        
        # است بعاد المهن غير المطلوبة
        if should_exclude(title):
            excluded_count += 1
            excluded_list.append(title)
            continue
        
        # تحديد الجنس
        is_female = is_female_profession(title)
        
        # تحديد الأوراق المطلوبة
        requirements = determine_requirements(title, is_female)
        
        # إنشاء كائن المهنة
        profession = {
            'code': code,
            'name_ar': title,
            'profession_name_ar': title,
            'profession_code': code,
            'category': 'أخرى',
            'gender': 'أنثى' if is_female else 'ذكر',
            'requirements': requirements
        }
        
        processed.append(profession)
    
    print(f"\n📊 الإحصائيات:")
    print(f"   - المهن المستبعدة: {excluded_count}")
    print(f"   - المهن المعالجة: {len(processed)}")
    print(f"\n❌ المهن المستبعدة:")
    for ex in excluded_list[:10]:
        print(f"      • {ex}")
    if len(excluded_list) > 10:
        print(f"      ... و {len(excluded_list) - 10} مهنة أخرى")
    
    # حفظ النتائج
    with open('/app/frontend/public/professions.json', 'w', encoding='utf-8') as f:
        json.dump(processed, f, ensure_ascii=False, indent=2)
    
    print(f"\n✅ تم الحفظ بنجاح!")
    
    return processed

if __name__ == '__main__':
    result = process_all_professions()
