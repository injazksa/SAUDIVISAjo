def get_requirements_engineer(is_female):
    """مهندسين"""
    base = get_requirements_commercial_female() if is_female else get_requirements_commercial_male()
    additions = [
        "التقديم على موقع مصادقة للشهادات الهندسية",
        "عضوية نقابة المهندسين الأردنيين ومزاولة مهنة سارية المفعول",
        "عضوية سارية المفعول من الهيئة السعودية للمهندسين"
    ]
    
    result = []
    for req in base:
        result.append(req)
        if "الشهادة الجامعية" in req:
            result.extend(additions)
    return result

# ========== المساعدات ==========

def is_female_title(title):
    """تحديد الجنس من العنوان"""
    female_patterns = ['ة ', ' ة', 'مساعدة', 'منسقة', 'بائعة', 'كاشيرة', 
                      'سكرتيرة', 'عاملة', 'خادمة', 'نادلة', 'موظفة']
    return any(p in title for p in female_patterns)

def should_exclude(title):
    """استبعاد المهن"""
    exclude_list = [
        # أطباء
        'طبيب', 'دكتور', 'طبيبة', 'رئيس الأطباء',
        # محامين وقضاة
        'محامي', 'محاماة', 'قاضي', 'قاض', 'محكم', 'كاتب عدل', 'كاتب ضبط',
        # حكومة وملكية
        'وزير', 'نائب وزير', 'وكيل وزارة', 'وكيل أمين', 'وكيل إمارة', 'وكيل محافظة',
        'حكومة', 'حكومي',
        'أمير', 'شيخ', 'ملك', 'محافظ', 'حاكم', 'عمدة',
        'أمين منطقة', 'أمين مجلس', 'وكيل',
        # إضافات
        'المفتي', 'النائب العام', 'عضو مجلس', 'عضو المجلس',
        'مندوب دائم', 'رئيس قرية', 'أمين عام', 
        'رئيس بلدية', 'سفير', 'قنصل', 'سكرتير دبلوماسي',
        'ملحق فني', 'رئيس منظمة', 'رئيس غرفة', 'رئيس اتحاد',
        'رئيس هيئة عامة'
    ]
    
    t = title.lower()
    
    # استبعاد أي شيء يبدأ بـ "وكيل"
    if t.startswith('وكيل'):
        return True
    
    # استبعاد أي شيء يحتوي على "أمين" (ما عدا أمين صندوق، أمين مستودع)
    if 'أمين' in t and 'صندوق' not in t and 'مستودع' not in t and 'سر' not in t and 'محفوظات' not in t and 'متحف' not in t and 'مكتبة' not in t and 'معرض' not in t:
        return True
    
    return any(ex in t for ex in exclude_list)

def determine_reqs(title, is_female):
    """تحديد المتطلبات"""
    t = title.lower()
    
    if t in ['رئيس تنفيذي', 'مدير عام']:
        return get_requirements_ceo()
    
    if 'سائق خاص' in t:
        return get_requirements_driver_male()
    
    if any(w in t for w in ['حلاق', 'طاهي', 'شيف', 'طباخ', 'خباز']):
        return get_requirements_barber()
    
    # العمال والمهن البسيطة (بائع، كاشير، منسق، إلخ)
    worker_kw = ['عامل', 'عاملة', 'حارس', 'نجار', 'حداد', 'سباك', 'دهان', 'كهربائي', 'نادل', 'بناء', 
                 'بائع', 'بائعة', 'كاشير', 'كاشيرة', 'منسق', 'منسقة', 'مساعد', 'مساعدة']
    if any(t.startswith(w) or w in t for w in worker_kw):
        return get_requirements_female() if is_female else get_requirements_worker_male()
    
    if 'مهندس' in t or 'هندسة' in t or 'هندسي' in t:
        return get_requirements_engineer(is_female)
    
    return get_requirements_commercial_female() if is_female else get_requirements_commercial_male()

