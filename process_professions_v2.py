def get_requirements_for_engineers(base_reqs):
    """إضافة متطلبات المهندسين"""
    # نستخدم متطلبات مدير تجاري كأساس ونضيف عليها
    engineer_specific = [
        "التقديم على موقع مصادقة للشهادات الهندسية",
        "عضوية نقابة المهندسين الأردنيين ومزاولة مهنة سارية المفعول",
        "عضوية سارية المفعول من الهيئة السعودية للمهندسين"
    ]
    
    # نضيف المتطلبات الخاصة بعد الشهادة الجامعية
    new_reqs = []
    for i, req in enumerate(base_reqs):
        new_reqs.append(req)
        if "الشهادة الجامعية" in req:
            new_reqs.extend(engineer_specific)
    
    return new_reqs

def determine_profession_requirements(title, code, category):
    """تحديد الأوراق المطلوبة حسب المهنة"""
    title_lower = title.lower()
    
    # 1. رئيس تنفيذي ومدير عام فقط
    if title_lower in ['رئيس تنفيذي', 'مدير عام']:
        return get_requirements_for_ceo()
    
    # 2. سائق خاص
    if 'سائق خاص' in title_lower:
        return get_requirements_for_driver()
    
    # 3. الاستقدام العائلي
    if 'استقدام' in title_lower or category == 'استقدام':
        return get_requirements_for_family_sponsorship()
    
    # 4. حلاق، طاهي، شيف (مزاولة مهنة)
    if any(word in title_lower for word in ['حلاق', 'طاهي', 'شيف', 'طباخ']):
        return get_requirements_for_barber()
    
    # 5. عمال (أي مسمى يبدأ بـ "عامل")
    if title_lower.startswith('عامل'):
        return get_requirements_for_workers()
    
    # 6. المهندسين
    if 'مهندس' in title_lower or 'هندسة' in title_lower:
        base_reqs = get_requirements_for_commercial_manager()
        return get_requirements_for_engineers(base_reqs)
    
    # 7. مدير تجاري (90% من المهن)
    # أي مهنة فيها: مدير، أخصائي، استشاري، مراجع داخلي
    if any(word in title_lower for word in ['مدير', 'أخصائي', 'استشاري', 'مراجع']):
        return get_requirements_for_commercial_manager()
    
    # 8. Default: مدير تجاري للباقي
    return get_requirements_for_commercial_manager()

def should_exclude(title):
    """استبعاد المهن الحكومية والملكية والمحامين والأطباء"""
    exclude_keywords = [
        'طبيب', 'دكتور',
        'محامي', 'محاماة', 'قاضي',
        'وزير', 'حكومة', 'حكومي',
        'شيخ', 'أمير', 'ملك', 'أميرة'
    ]
    
    title_lower = title.lower()
    return any(kw in title_lower for kw in exclude_keywords)

def process_all_professions():
    """معالجة جميع المهن"""
    # قراءة الملف الأصلي
    with open('/app/EnjazKSA-main/professions.json', 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    print(f"عدد المهن الأصلي: {len(data)}")
    
    processed = []
    excluded_count = 0
    
    for item in data:
        title = item.get('title', item.get('name', 'غير محدد'))
        code = item.get('code', '')
        category = item.get('category', '')
        
        # استبعاد المهن غير المطلوبة
        if should_exclude(title):
            excluded_count += 1
            continue
        
        # تحديد الأوراق المطلوبة
        requirements = determine_profession_requirements(title, code, category)
        
        # إنشاء كائن المهنة
        profession = {
            'code': code,
            'title': title,
            'profession_name_ar': title,
            'profession_code': code,
            'category': category,
            'requirements': requirements
        }
        
        processed.append(profession)
    
    print(f"المهن المستبعدة: {excluded_count}")
    print(f"المهن المعالجة: {len(processed)}")
    
    # حفظ النتائج
    with open('/app/frontend/public/professions.json', 'w', encoding='utf-8') as f:
        json.dump(processed, f, ensure_ascii=False, indent=2)
    
    print("✅ تم الحفظ بنجاح!")
    
    # عرض عينات
    print("\n=== عينات ===")
    for item in processed[:3]:
        print(f"\n{item['code']}: {item['title']}")
        print(f"  عدد الأوراق: {len(item['requirements'])}")

if __name__ == '__main__':
    process_all_professions()
